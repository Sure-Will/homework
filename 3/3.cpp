#include <stdio.h>

int main()
{
	int matrix[3][3];//定义一个3行3列的二维数组，用来存储矩阵的9个元素
	int sum = 0;//定义变量sum用来累加主对角线元素的和，初始值为0
	int i, j; // 定义循环变量i和j，i表示行号，j表示列号

	printf("请在下方输入3×3矩阵中的九个元素:\n");

	//第一步：使用双层循环依次输入矩阵的每个元素
	for (i = 0; i < 3; i++)//外层循环控制行，i从0到2，共3行
	{
		for (j = 0; j < 3; j++)//内层循环控制列，j从0到2，共3列
		{
			printf("输入第%d行第%d列的元素：", i + 1, j + 1);//提示当前要输入的位置（i+1和j+1是因为人们习惯从1开始数）
			scanf_s("%d", &matrix[i][j]);//读取用户输入的整数，存储到矩阵的第i行第j列
		}
	}

	//第二步：计算主对角线元素之和
	for (i = 0; i < 3; i++)//循环3次，i分别为0、1、2
	{
		sum = sum + matrix[i][i];//把matrix[0][0]、matrix[1][1]、matrix[2][2]依次加到sum中（行号等于列号就是主对角线）
	}

	printf("\n主对角线之和为:%d\n", sum);//输出最终计算结果

	return 0;
}