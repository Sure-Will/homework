#include <iostream>
#include <vector>
using namespace std;

// 辅助函数：打印 vector 的内容
void printVector(const vector<int>& v, const string& name) {
    cout << name << " 包含：";
    if (v.empty()) {
        cout << "（空）";
    }
    for (int i = 0; i < v.size(); i++) {
        cout << v[i] << " ";
    }
    cout << "  [共 " << v.size() << " 个元素]" << endl;
}

int main() {
    // =============================================
    // 方法1：默认初始化 - 创建一个空的 vector
    // =============================================
    vector<int> v1;  // 不带任何参数，创建空容器
    printVector(v1, "v1");

    // =============================================
    // 方法2：指定元素个数，每个元素都是默认值（int默认是0）
    // =============================================
    vector<int> v2(5);  // 创建包含5个元素的vector，每个都是0
    printVector(v2, "v2");

    // =============================================
    // 方法3：指定元素个数和初始值
    // =============================================
    vector<int> v3(5, 10);  // 创建5个元素，每个都是10
    printVector(v3, "v3");

    // =============================================
    // 方法4：列表初始化（使用花括号）
    // =============================================
    vector<int> v4 = { 1, 2, 3, 4, 5 };  // 用具体的值初始化
    printVector(v4, "v4");

    // =============================================
    // 方法5：拷贝另一个 vector
    // =============================================
    vector<int> v5(v4);  // 把 v4 的内容完整复制给 v5
    printVector(v5, "v5");

    // =============================================
    // 方法6：使用迭代器范围初始化
    // =============================================
    vector<int> v6(v4.begin(), v4.begin() + 3);  // 从v4复制前3个元素
    printVector(v6, "v6");

    return 0;
}
